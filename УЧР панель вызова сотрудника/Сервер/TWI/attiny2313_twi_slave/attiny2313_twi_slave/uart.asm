;========================================================
;		Процедура отправки байта из регистра BYTE
;========================================================
USART_Transmit:	
	sbis 	UCSRA, UDRE		; пропуск если нет флага готовности
	rjmp	USART_Transmit 	; ждем готовности - флага udre
 
	out		UDR, BYTE	; шлём байт

	ret					
